package org.example.e17;

public class E17NegativeModulusCalculation {
    public static void main(String[] args) {
        // Declare the int variables and assign values
        int dividend = -21; // example value
        int divisor = 4; // example value

        // Calculate modulus
        int modulusResult = dividend % divisor;

        // Print the result
        System.out.println(dividend + " mod " + divisor + " = " + modulusResult);
    }
}
