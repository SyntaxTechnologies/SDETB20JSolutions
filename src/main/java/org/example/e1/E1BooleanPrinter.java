package org.example.e1;

public class E1BooleanPrinter {
    public static void main(String[] args) {

        // Step 1: Create Variables
        boolean isTrue;
        boolean isFalse;

        // Step 2: Assign Values
        isTrue = true;
        isFalse = false;

        // Step 3: Print Values
        System.out.println(isTrue);
        System.out.println(isFalse);

    }
}